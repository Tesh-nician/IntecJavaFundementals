package be.intecbrussel.Les2;

public class AssignmentExample {
    public static void main(String[] args) {

        //initialisation
        int a = 10;
        int b = 20;
        int c = 0;


        c = (a+b);//add
        System.out.println(c);
        c+=a; //a+c
        System.out.println(c);
        c-=a; // c-a
        System.out.println(c);
        c*=a;// c*a
        System.out.println(c);
        c/=a;// c/a
        System.out.println(c);
        c%=a; // c mod a
        System.out.println(c);
    }
}
